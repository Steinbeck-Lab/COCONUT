                            </h1>
                            <p class="mb-4">
                                COCONUT Online is an open-source project that stores,
                                searches, and analyzes Natural Products (NPs). The
                                database contains over 50 open NP resources, and the
                                data is accessible free of charge and without any
                                restrictions. Each entry corresponds to a "flat" NP
                                structure and is associated with their known
                                stereochemical forms, literature, organisms that produce
                                them, natural geographic presence, and precomputed
                                molecular properties if available.
