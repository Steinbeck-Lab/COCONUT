                                <h2 class="mt-6 text-xl font-semibold leading-7 text-indigo-600 sm:text-xl">
                                    Advanced search
                                </h2>
                                <p>
                                    Advanced search enables users to search for a
                                    compound of their interest by specifying structural
                                    properties and molecular descriptors. Molecular
                                    descriptors represent the properties of the
                                    molecules that are computed by algorithms. The
                                    numerical values of molecular descriptors are used
                                    to quantitatively express the physical and chemical
                                    characteristics of the molecules. Another advantage
                                    of advanced search is the ability to select the
                                    search in one of the data sources used to compile
                                    the overall database of our site.
                                </p>
