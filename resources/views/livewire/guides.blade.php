                                    <h2 class="mt-6 text-xl font-semibold leading-7 text-indigo-600 sm:text-xl">
                                        Substructure search
                                    </h2>
                                    <p class="mb-4">
                                        A suitable way of searching for a particular
                                        compound when one of its parts is known to the
                                        user. Both, InChl and Canonical SMILES
                                        structural formulas may be used in substructure
                                        search. There are three ways of our software
                                        computation during the search operation:
                                    </p>
                                    <ul class="list-disc list-inside">
                                        <li>
                                            <strong class="font-semibold text-gray-900">Default substructure search
                                                (Ullmann
                                                algorithm).</strong>
                                            The Ullmann algorithm is a backtracking
                                            procedure that utilizes a relaxation-based
                                            reï¬nement step to reduce the search space.
                                            The refinement is the most important step of
                                            the algorithm. It basically evaluates the
                                            surrounding of every node in the database
                                            molecules and compares them with the entered
                                            substructure.
                                        </li>
                                        <li>
                                            <strong class="font-semibold text-gray-900">Substructure search with
                                                depth-first
                                                (DF) pattern.</strong>
                                            The DF algorithm executes the search
                                            operation of the entered molecule in a
                                            depth-first manner (bond by bond).
                                            Therefore, this algorithm utilizes
                                            backtracking search iterating over the bonds
                                            of entered molecules.
                                        </li>
                                        <li>
                                            <strong class="font-semibold text-gray-900">Substructure search with the
                                                Vento-Foggia algorithm.</strong>
                                            The Vento-Foggia algorithm iteratively
                                            extends a partial solution using a set of
                                            feasibility criteria to decide whether to
                                            extend or backtrack. In the Ullmann
                                            algorithm, the node-atom mapping is fixed in
                                            every step. In contrast, the Vento-Foggia
                                            algorithm iteratively adds node-atom pairs
                                            to a current solution. In that way, this
                                            algorithm directly discovers the topology of
                                            the substructure and seeks for all natural
                                            products that contain the entered
                                            substructure.
                                        </li>
                                    </ul>
                                </div>
                                <p>
                                    In all three cases, the software output is a group
                                    of compounds that contain drawn substructures.
                                </p>
